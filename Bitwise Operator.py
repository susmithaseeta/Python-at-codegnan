print()

print("Right Shift: a>>1 =", a>>1)
print("Right Shift: a>>2 =", a>>2)
print()

print("Bitwise and: a&b =", a&b)
print("Bitwise and: 5&3 =", 5&3)
print()

print("Bitwise or: a|b =", a|b)
